CREATE DATABASE elmain_boston;
USE elmain_boston;

-- 1. Customer Table
CREATE TABLE Customer (
    Customer_ID INT PRIMARY KEY,
    First_Name VARCHAR(50) NOT NULL,
    Last_Name VARCHAR(50),
    Address VARCHAR(200),
    Email VARCHAR(70) UNIQUE, 
    State VARCHAR(50),
    Zip_Code INT,
    Date_of_Birth DATE,
    Mobile_Number VARCHAR(15)
);

-- 2. Designer Table
CREATE TABLE Designer (
    Designer_ID VARCHAR(6) PRIMARY KEY,
    Designer_Name VARCHAR(100) NOT NULL,
    Rating DECIMAL(2,1),
    No_Customers_Designed INT,
    Experience_Level INT
);

-- 3. Tailor Table
CREATE TABLE Tailor (
	Tailor_ID VARCHAR(6) PRIMARY KEY,
    Tailor_Name VARCHAR(100) NOT NULL,
    Locality VARCHAR(50),
    State VARCHAR(50),
    Zip_Code INT,
    Rating DECIMAL (2,1),
    Certification_Status VARCHAR(4),
    No_Customers_Worked INT,
    Experience INT
);

-- 4. Product Table 
CREATE TABLE Product (
    Product_ID VARCHAR(6) PRIMARY KEY,
    Product_Name VARCHAR(100) NOT NULL,
    Category VARCHAR(50),
    Price DECIMAL(5, 2),
    Designer_ID VARCHAR(6) NOT NULL,
    Customer_Rating DECIMAL(2, 1),
    FOREIGN KEY (Designer_ID) REFERENCES Designer(Designer_ID)
);

-- 5. Store Table 
CREATE TABLE Store (
    Store_ID VARCHAR(6) PRIMARY KEY,
    Store_Location VARCHAR(100),
    Contact_Info VARCHAR(15),
    State VARCHAR(50),
    Zip_Code INT
);

-- 6. Inventory Table 
CREATE TABLE Inventory (
    Store_ID VARCHAR(6),
    Product_ID VARCHAR(6),
    Quantity INT,
    PRIMARY KEY (Store_ID, Product_ID),
    FOREIGN KEY (Store_ID) REFERENCES Store(Store_ID),
    FOREIGN KEY (Product_ID) REFERENCES Product(Product_ID)
);

-- 7. Ready_Made Table 
CREATE TABLE Ready_Made (
    Ready_Made_ID VARCHAR(6) PRIMARY KEY,
    Product_ID VARCHAR(6),
    Quantity INT,
    FOREIGN KEY (Product_ID) REFERENCES Product(Product_ID)
);

-- 8. Measurement Table 
CREATE TABLE Measurement (
    Measurement_ID VARCHAR(6) PRIMARY KEY,
    Length DECIMAL(5,2),
    Width DECIMAL(5, 2)
);

-- 9. Customized Table
CREATE TABLE Customized (
    Custom_ID VARCHAR(6) PRIMARY KEY,
    Tailor_ID VARCHAR(6),
    Designer_ID VARCHAR(6),
    Measurement_ID VARCHAR(6),
    FOREIGN KEY (Measurement_ID) REFERENCES Measurement(Measurement_ID),
    FOREIGN KEY (Tailor_ID) REFERENCES Tailor(Tailor_ID),
    FOREIGN KEY (Designer_ID) REFERENCES Designer(Designer_ID)
);

-- 10. Shipping Table
CREATE TABLE Shipping (
	Shipping_ID VARCHAR(6) PRIMARY KEY,
    Shipping_Partner VARCHAR(100),
    Tracking_Number VARCHAR(50)
);

-- 11. Pickup Table
CREATE TABLE Pickup (
    Pickup_ID VARCHAR(6) PRIMARY KEY,
    Store_ID VARCHAR(6),
    Pickup_Date DATE,
    Pickup_Time_Slot TIME,
    FOREIGN KEY (Store_ID) REFERENCES Store(Store_ID)
);

-- 12. Delivery Table
CREATE TABLE Delivery (
    Delivery_ID VARCHAR(6) PRIMARY KEY,
    Delivery_Type ENUM('Shipping', 'Pickup') NOT NULL,
    Date_Of_Delivery DATE,
    Shipping_ID VARCHAR(6) NULL,
    Pickup_ID VARCHAR(6) NULL,
    FOREIGN KEY (Shipping_ID) REFERENCES Shipping(Shipping_ID),
    FOREIGN KEY (Pickup_ID) REFERENCES Pickup(Pickup_ID)
);

-- 13. Orders Table 
CREATE TABLE Orders (
    Order_ID VARCHAR(6) PRIMARY KEY,
    Customer_ID INT,
    Order_Date DATE,
    Sale_Type ENUM('Customized', 'Ready-made') NOT NULL,
    Ready_Made_ID VARCHAR(6) NULL,
    Custom_ID VARCHAR(6) NULL,
    Delivery_ID VARCHAR(6) NOT NULL,
    FOREIGN KEY (Ready_Made_ID) REFERENCES Ready_Made(Ready_Made_ID),
    FOREIGN KEY (Custom_ID) REFERENCES Customized(Custom_ID),
    FOREIGN KEY (Customer_ID) REFERENCES Customer(Customer_ID),
    FOREIGN KEY (Delivery_ID) REFERENCES Delivery(Delivery_ID)
);

